    public void next(){
        System.out.println("You have a current score of " + game.currentPlayer.playerScore + ". Click your button to refresh your score!");
        if(game.currentPlayer.getName() == game.p1.getName()){
            game.currentPlayer = game.p2;
        }else if(game.currentPlayer.getName() == game.p2.getName()){
            game.currentPlayer = game.p1;
        }
        System.out.println("It is now " + game.currentPlayer.getName() + "'s turn");
        resetBorder();
        resetDice();
    }
    
