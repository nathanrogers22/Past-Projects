    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Add the dice roll sound effect
        jButton2.setToolTipText("Click this button to roll");
        InputStream music;
        try{
            music = new FileInputStream(new File("C:\\Users\\ntrog\\OneDrive\\Documents\\NetBeansProjects\\Project 1\\src\\project\\pkg1\\Dice Roll Sound Effect.wav"));
            AudioStream rolling = new AudioStream(music);
            AudioPlayer.player.start(rolling);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
        }
        game.rollDice();
        changeDiceIcon();
        System.out.println("Select the dice you would like to keep?");
        if(game.d1.isKept && game.d2.isKept && game.d3.isKept && game.d4.isKept && game.d5.isKept && game.d6.isKept){
            System.out.println("Looks like you have selected all the dice, click the NEXT button to bank your score");
        }else{
            System.out.println("If you would like to roll again click the roll button or click the next button to bank your current score");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed
